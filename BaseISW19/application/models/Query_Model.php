 /* =============================================================================================================================================================================================================================== */

 /* START - CONTROLLER: Usuarios */

function InsertUsuario($datosusuario)
{
  $this->db->insert('usuarios', $datosusuario);
}

function DatosUsuario()
{
  $this->db->select('*');
  $this->db->from('usuarios');
  $query = $this->db->get();
  return $query->result();

}

